let day = "Monday";  // Declare and assign a string value representing a day of the week

switch (day) {
  case "Monday":
    console.log("Monday: Start of the week");
    break;
  case "Tuesday":
    console.log("Tuesday: Workday");
    break;
  case "Wednesday":
    console.log("Wednesday: Midweek");
    break;
  case "Thursday":
    console.log("Thursday: Almost there!");
    break;
  case "Friday":
    console.log("Friday: TGIF");
    break;
  case "Saturday":
    console.log("Saturday: Weekend!");
    break;
  case "Sunday":
    console.log("Sunday: Relaxation day");
    break;
  default:
    console.log("Invalid day");
    break;
}
